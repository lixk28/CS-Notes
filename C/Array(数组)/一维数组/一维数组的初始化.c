/* 一维数组的初始化 */ 
#include<stdio.h>

int main()
{
	int a[5]={1,2,3,4,5}; // 在定义数组时对数组所有元素赋初值 
	                      // 将初值放在花括号内，用逗号间隔
						   
	int b[5]={1,2,3};  //可以只给一部分元素赋初值  未被赋初值的元素系统自动给其赋值为 0 
	
	int c[]={1,2,3}; // 若对全部元素赋初值，由于数据个数确定，可以不指定数组长度
	                 // 数组c长度自动分配为 3
	                 
	/* 也可以通过循环结构对数组元素初始化 */
	int i;
	int n[10];
	for(i=0;i<=9;i++)  //注意是 0-9 ！ 
	  a[i]=i;
	for(i=0;i<=9;i++)
	  printf("%d  ",a[i]);	
	  			  
	return 0;
}
